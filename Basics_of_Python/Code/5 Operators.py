# Type of Operators

value1,value2 = eval(input("Enter the numbers"))
print(value1, '+', value2, '=',value1+value2)

print(value1, '-', value2, '=',value1-value2)

print(value1, '*', value2, '=',value1*value2)

print(value1, '/', value2, '=',value1/value2)

print(value1, '%', value2, '=',value1%value2)


# Boolean Operators
x = True
y = False

print("x or y is", x or y)

print("x and y is", x and y)

print("y not is", not y)

print("x not is", not x)














