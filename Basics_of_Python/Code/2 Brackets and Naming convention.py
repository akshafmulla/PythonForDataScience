# Brackets in Python
# There are 3 types i.e.
# 1 List which is denoted by square brackets []. This is similar to array.
# 2 Dictionary which is denoted by curly brackets {}. This is similar to JSON.
# 3 Tuple which is denoted by parenthesis i.e. round bracket (). This is similar to method parameters.

list1 = [1,2,3,4]
dictionary = {'Name' : 'Akshaf','Number' : 89889}
tuple1 = (1,2,3)
print(list1,dictionary, tuple1)


